liczba = 50

if liczba % 3 == 0:  # operator % zwraca resztę z dzielenia, == oznacza sprawdzenie czy wartości są równe
    print(f'Liczba {liczba} jest podzielna przez 3')

if liczba % 5 == 0:  # operator % zwraca resztę z dzielenia, == oznacza sprawdzenie czy wartości są równe
    print(f'Liczba {liczba} jest podzielna przez 5')

